// toExponential() returns a string, with a number rounded and written using exponential notation.

let x = 9.656;
let a = x.toExponential(2);
let b = x.toExponential(4);
let c = x.toExponential(6);
console.log(a);
console.log(b);
console.log(c);

console.log(typeof(x));
