// toFixed() returns a string, with the number written with a specified number of decimals:
let x = 9.656;
let a = x.toFixed(0);
let b = x.toFixed(2);
let c = x.toFixed(4);
let d = x.toFixed(6);
console.log(a);
console.log(b);
console.log(c);
console.log(d);

console.log(typeof(x));
